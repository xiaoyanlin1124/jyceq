package com.jy.common.device.AppPush;

/**
 *	推送平台识别
 * @author lamaq
 *
 */
public class PushPlatform {
	
	//信鸽
	private String xg ;
	//小米
	private String mi ;
	//华为
	private String hw ;
	//魅族
	private String mz ;
	//FCM
	private String fcm;
	
	public String getXg() {
		return xg;
	}
	public void setXg(String xg) {
		this.xg = xg;
	}
	public String getMi() {
		return mi;
	}
	public void setMi(String mi) {
		this.mi = mi;
	}
	public String getHw() {
		return hw;
	}
	public void setHw(String hw) {
		this.hw = hw;
	}
	public String getMz() {
		return mz;
	}
	public void setMz(String mz) {
		this.mz = mz;
	}
	public String getFcm() {
		return fcm;
	}
	public void setFcm(String fcm) {
		this.fcm = fcm;
	}

}
